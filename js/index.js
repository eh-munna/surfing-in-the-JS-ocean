// JavaScript For Loop

// Basic for loop

const letters = ['a', 'b', 'c', 'd', 'e', 'f'];
for (let index = 0; index < letters.length; index++) {
  console.log(letters[index]);
}

console.log(`----Reverse-----`);

// Reverse for loop
for (let index = letters.length - 1; index >= 0; index--) {
  console.log(letters[index]);
}
